    public void TabBukuSelectedRow(){
        tKodeBuku.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),0)));
        tNamaBuku.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),1)));
        tJumlahBuku.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),2)));
        tPenulis.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),4)));
        tPenerbit.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),5)));
        tKodeKategori.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),6)));
        tKodeTataLetak.setText(String.valueOf(tabBuku.getValueAt(tabBuku.getSelectedRow(),7)));
    }
