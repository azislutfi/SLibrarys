public void addListener(ActionListener e) {
        btKembali.addActionListener(e);
        btReset.addActionListener(e);
        btTambah.addActionListener(e);
        btHapus.addActionListener(e);
        btUpdate.addActionListener(e);
        btCariBuku.addActionListener(e);
        cbCariBuku.addActionListener(e);
        btCariKategori.addActionListener(e);
        cbCariKategori.addActionListener(e);
        btCariTataLetak.addActionListener(e);
        cbCariTataLetak.addActionListener(e);
        tabBuku.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e)  {
         //       try {
                    TabBukuSelectedRow();
        //        } catch (ParseException ex) {
                   
        //            }
                tKodeBuku.setEditable(false);
                btTambah.setEnabled(false);
                btHapus.setEnabled(true);
                btUpdate.setEnabled(true);
            }            
        });
        tabKategori.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e)  {
                TabKategoriSelectedRow();
            }            
        });
        tabTataLetak.addMouseListener(new MouseAdapter(){
            public void mouseClicked(MouseEvent e)  {
                try {
                    TabTataLetakSelectedRow();
                } catch (ParseException ex) {
                    Logger.getLogger(gBuku.class.getName()).log(Level.SEVERE, null, ex);
                }
            }            
        }); 
    }
